"""Задача 1
Напишіть програму яка запитує в користувача вартість покупок,
він їх вводе через пробіл, точна кількість не вказана.
Вам потрібно до вартості покупок додати 6,5 відсотки податків.

Потім питаєте чи є в користувача купон на знижку якщо є то питаєте це на суму чи на відсоток і потім віднімаєте суму
або відсоток відповідно від ціни яку отримали раніше і пишете користувачу кінцеву суму.

* завдання з зірочкою не впливає на бал. Округліть суму якщо копійок більше ніж 44 то це буде + 1 гривня,
якщо менше. то тоді просто відкидаємо копійки від ціни."""

# Запит користувача вартості покупок
purchase_cost = input('Введіть вартість ваших покупок через пробіл: ')
purchase_items = []

# Розділення введеної вартості покупок і обчислення загальної суми
split_purchase = purchase_cost.split()
for item in split_purchase:
    purchase_items.append(float(item))
total_cost = sum(purchase_items)

# Додавання податків до загальної суми
total_cost_with_tax = total_cost + 0.065 * total_cost
# Запит користувача про наявність купона на знижку
discount_coupon = input("Чи є у вас купон на знижку? (yes / no): ")
# Якщо є купон на знижку
if discount_coupon.lower() == 'yes':
    # Запит користувача про тип купона (сума чи відсоток)
    discount_type = input("Купон на знижку за сумою чи відсотком? (coupon / %): ")

    # Якщо купон на знижку за сумою
    if discount_type.lower() == 'coupon':
        discount_amount = float(input("Введіть суму знижки: "))
        print(f'Загальна сума зі знижкою: {total_cost_with_tax - discount_amount}')
    # Якщо купон на знижку відсотком
    elif discount_type.lower() == '%':
        discount_percent = float(input("Введіть відсоток знижки: "))
        discount = discount_percent / 100 * total_cost_with_tax
        print(f'Загальна сума зі знижкою: {total_cost_with_tax - discount}')
    else:
        print("Невірний тип купона.")
else:
    print(f'Загальна сума без знижки: {total_cost_with_tax}')

