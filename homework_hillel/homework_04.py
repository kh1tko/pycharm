USER_PIN_CODE = 0000
n = 3

while n > 0:
    check_pin = int(input("Введіть ваш пін для перевірки: "))

    if USER_PIN_CODE == check_pin:
        print("PIN вірний. Вхід дозволено.")
        break
    else:
        n -= 1
        print(f"Невірний PIN. Залишилося спроб: {n}")

if n == 0:
    print("Ви вичерпали всі спроби. Карта заблокована.")

