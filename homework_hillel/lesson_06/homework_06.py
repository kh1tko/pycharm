# Запитайте у користувача довжини катетів та виведіть на екран довжину гіпотенузи.
# Трикутник рівнобедрений. Що б взвести в степінь ставимо два рази множення
# два в степені три буде так 2**3, квадратний корінь з двух буде 2**(1/2)
# Запитайте у користувача довжини катетів

# Функція що приймає значення двох катетів та повертає чому дорівнює гіпотенуза
def calculate_hypotenuse(leg_1: int | float, leg_2: int | float):
    hypotenuse = (leg_1 ** 2 + leg_2 ** 2) ** (1 / 2)
    return (f'Довжина гіпотенузи рівнобедреного '
            f'трикутника з катетами {leg_1} і {leg_2} дорівнює {round(hypotenuse, 2)}')


