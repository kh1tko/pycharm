# Функція що приймає вік відвідувача кінотеатру та обробляє який меседж повернути при обробці його віку
def cinema_cashier(age: int):
    if age <= 7:
        return f'Де твої батьки? Це, мабуть, ще один мультфільм!'
    elif age <= 16:
        return f'Це фільм для дорослих! Нам потрібно перевірити твоє посвідчення.'
    elif 16 <= age < 65:
        return f'Вітаємо на сеансі! Насолоджуйтесь фільмом, і не забудьте попкорн!'
    else:
        return f'Покажіть пенсійне посвідчення! Можливо, у нас є знижки для вас.'


